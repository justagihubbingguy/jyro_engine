package net.jyro.windows.graphics.TB11.matrixsys;

public class Vec4 {
    public float x, y, z, w;
    public int color;

    public Vec4(float x, float y, float z, float w, int color) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
        this.color = color;
    }
}
